def addition(a,b):
    return a+b
def substraction(a,b):
    return a-b
def multiplication(a,b):
    return a*b
def division(a,b):
    return a/b
def floordivision(a,b):
    return a//b
def modulus(a,b):
    return a%b
def power(a):
    return a**a
def cube(a):
    return a**3

def Areaofsqr(side):
    return side*side

def Areaofcircle(a):
    return 3.14*a*a

def AreaofTraingle(l,b):
    return l*b
